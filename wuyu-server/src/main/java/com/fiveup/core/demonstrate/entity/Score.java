package com.fiveup.core.demonstrate.entity;

public class Score {
    private String id;
    private String name;
    private String riqi;
    private int deyu;
    private int zhiyu;
    private int tiyu;
    private int meiyu;
    private int laoyu;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRiqi() {
        return riqi;
    }

    public void setRiqi(String riqi) {
        this.riqi = riqi;
    }

    public int getDeyu() {
        return deyu;
    }

    public void setDeyu(int deyu) {
        this.deyu = deyu;
    }

    public int getZhiyu() {
        return zhiyu;
    }

    public void setZhiyu(int zhiyu) {
        this.zhiyu = zhiyu;
    }

    public int getTiyu() {
        return tiyu;
    }

    public void setTiyu(int tiyu) {
        this.tiyu = tiyu;
    }

    public int getMeiyu() {
        return meiyu;
    }

    public void setMeiyu(int meiyu) {
        this.meiyu = meiyu;
    }

    public int getLaoyu() {
        return laoyu;
    }

    public void setLaoyu(int laoyu) {
        this.laoyu = laoyu;
    }

    public Score() {

    }
}
